				<h1 style="color: white;">Don Giovanni.</h1>
			</center>
			<div align="justify" style="color: white;">
				Esta ópera, de Wolfgang A. Mozart, es un dramma giocoso -término que denota una mezcla de acción cómica y seria- que estrenó en el Teatro de Praga, en 1787. Su libreto en italiano es de Lorenzo da Ponte, quien remota la obra de Antonio de Zamora.
				<br>
				<br>
				Por sus características, Mozart introdujo la obra en su catálogo como una ópera bufa. La obra, destacada del repertorio operístico estándar, es una de las más reconocidas del compositor austriaco después de La flauta mágica y Las bodas de Fígaro.
				<br>
				<br>
				Su tema, una reelaboración de la obra de José Zorrilla, Don Juan, sucede en Sevilla cuando Don Giovanni, un joven noble, arrogante y promiscuo, se encuentra con alguien a quien no puede matar, eludir o burlar.
