				<h1 style="color: white;">Las bodas de Fígaro</h1>
			</center>
			<div align="justify" style="color: white;">
				De Wolfgang A. Mozart, se trata de una opera buffa -ópera de comedia- en cuatro actos con libreto en italiano de Lorenzo da Ponte, basado en Le mariage de Figaro de Pierre Augustin Caron de Beaumarchais. La pieza, considerada una de las más importantes de la historia de la música, estrenó en Viena, 1786, bajo la dirección de su compositor.
				<br>
				<br>
				Su historia, que se desarrolla en Sevilla a finales del siglo XVIII, fue adaptada por Da Ponte y Mozart para evitar las alusiones sociales y políticas del texto de Pierre Augustin, las cuales podían ser problemáticas en su tiempo; en su lugar, los creadores profundizaron en la caracterización de los personajes.
