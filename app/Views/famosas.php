				<h1 style="color: white;">Tosca</h1>
			</center>
			<div align="justify" style="color: white;">
				Con música de Giaccomo Puccini, es una ópera de tres actos con libreto en italiano de Luigi Illica y Giuseppe Giacosa. Esta pieza es una de las más destacadas dentro del repertorio verista italiano, fue estrenada en Roma, 1900, donde recibió la exitosa aceptación de su audiencia en el Teatro Costanzi.
				<br>
				<br>
				Su texto, basado en La Tosca de Victorien Sardou, está lleno de amor, intriga, pasión, violencia y muerte, con arias consideradas las más bellas del repertorio. La trama sucede en Roma, 1800, cuando Napoleón Bonaparte vence a los austriacos al mando del general Michael von Melas en la batalla de Marengo.
