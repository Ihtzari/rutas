				<h1 style="color: white;">Carmen</h1>
			</center>
			<div align="justify" style="color: white;">
				Basada en la novela Carmen de Prosper Mérimée, 1845, la ópera está integrada por cuatro actos de Georges Bizet, con libreto en francés de Ludovic Halévy y Henri Meilhac. Con críticas mayormente negativas, la ópera se estrenó en la Opéra-Comique de París en 1875. Sin saber la magnitud de su popularidad, Carmen alcanzó el éxito del público y la crítica en una producción de Viena, en 1875.
				<br>
				<br>
				Protagonizada por una gitana, la historia de Carmen se sitúa en Sevilla, alrededor de 1820. La mujer, de temperamento salvaje, seduce a don José, un soldado inexperto que invadido por los celos comete el asesinato de su propia amada.
