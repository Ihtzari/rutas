				<h1 style="color: white;">La bohème</h1>
			</center>
			<div align="justify" style="color: white;">
				Con música de Giaccomo Puccini y libreto en italiano de Giuseppe Giacosa y Luigi Illica, la ópera la componen cuatro actos basados en diferentes episodios de la novela Escenas de la vida bohemia de Henry Murger. La trama expone las vivencias de su compositor durante sus años de estudiante en el conservatorio de Milán, donde compartió habitación con el músico italiano Pietro Mascagni, uno de los mayores exponentes de la ópera verista.
				<br>
				<br>
				El montaje estrenó en el Teatro Regio de Turín, 1896, bajo la dirección del joven Arturo Toscanini, posteriormente el más grande director de orquesta de su época y del siglo XX. La bohème, considerada la obra más bella de Puccini, mezcla el drama y la comedia, y posee una de las muertes más emblemáticas en la ópera.
