				<h1 style="color: white;">La Traviata</h1>
			</center>
			<div align="justify" style="color: white;">
				Del compositor romántico italiano de ópera del siglo XIX, Giuseppe Verdi, es una ópera en tres actos con libreto en italiano de Francesco Maria Piave, inspirado en la novela La dama de las camelias de Alexandre Dumas (hijo). Titulada originalmente Violetta -nombre de la protagonista-, La Traviata estrenó en el año de 1853 en Venecia, presentación que, irónicamente, resultó un fracaso. Su historia está basada en la vida de una famosa cortesana parisiense de nombre Alphonsine Plessis, cuya vida es considerada como uno de los mayores exponentes del llamado Romanticismo francés.
