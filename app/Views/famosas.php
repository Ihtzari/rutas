				<h1 style="color: white;">El barbero de Sevilla</h1>
			</center>
			<div align="justify" style="color: white;">
				Ópera bufa de Gioacchino Rossini en dos actos con música de Gioachino Rossini y libreto en italiano de Cesare Sterbini. Basada en la comedia del mismo nombre de Pierre-Augustin de Beaumarchais, la pieza estrenó en 1816, en el Teatro Argentina de Roma. En 1825, se presentó en el Park Theater de Nueva York, convirtiéndose en una de las primeras óperas italianas que se representaron en Estados Unidos.
				<br>
				<br>
				Ha sido una de las grandes obras maestras de la comedia, escrita como la ópera bufa de todas las óperas bufas. Su historia cuenta las peripecias del conde de Almaviva y la joven huérfana Rosina, una pareja de enamorados.
